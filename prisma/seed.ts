async function main() {
  console.log("Seeding Eventure database with Gold Coast events...");

  for (const event of events) {
    const slug = slugify(event.name);
    await prisma.event.upsert({
      where: { source_sourceId: { source: event.source, sourceId: event.sourceId } },
      update: { ...event, slug, status: "published", lastScrapedAt: new Date() },
      create: { ...event, slug, status: "published", lastScrapedAt: new Date() },
    });
    console.log(`  + ${event.name}`);
  }

  console.log(`\nSeeded ${events.length} events.`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
